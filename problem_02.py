def main():
    # Introduce the program
    print("Square the Number Program")
    
    # Obtain user input
    number = int(input("Enter a number: "))

    # Compute and display the square of the number
    print(f"The square of {number} is {number * number:.2f}")

if __name__ == "__main__":
    main()